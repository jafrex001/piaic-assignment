let age: number = 18;

if (age < 2) {
    console.log("You are a baby");
} else if (age >= 2 && age < 4) {
    console.log("You are a toddler");
} else if (age >= 13 && age < 20) {
    console.log("You are a teenager");
} else if (age >= 20 && age < 65) {
    console.log("You are an adult");
} else {
    console.log("You are an elder");
}